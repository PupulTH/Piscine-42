#include <unistd.h>
#include <stdio.h>

void	ft_putnbr_base_print(int nbr, char *base, int size)
{
	unsigned int	n;
	char			a;

	if (nbr < 0)
	{
		write(1, "-", 1);
		n = nbr * (-1);
	}
	else
		n = nbr;
	if (n > (unsigned int)size)
	{
		ft_putnbr_base_print(n / size, base, size);
		a = base[n % size];
		write(1, &a, 1);
	}
	if (n >= 0 && n <= (unsigned int)size)
	{
		a = base[n % size];
		write(1, &a, 1);
	}
}

int	ft_error(char *base)
{
	int	i;

	i = 0;
	while (base[i] != '\0')
	{
		if (base[i] == '+' || base[i] == '-' || base[i] == base[i + 1]
			|| (base[i] < 32) || base[i] >= 127)
			return (0);
		i++;
	}
	if (i <= 1)
		return (0);
	return (1);
}

void	ft_putnbr_base(int nbr, char *base)
{
	int	i;

	i = 0;
	if (ft_error(base) == 1)
	{
		while (base[i] != '\0')
			i++;
		ft_putnbr_base_print(nbr, base, i);
	}
}

